    <span class="subtitle">Mono Cliffs</span>
    <br><br>
    <p><strong>Difficulty : </strong>Intermediate</p>
    <p><strong>Duration : </strong>2 1/2 hrs</p>
    <p><strong>Location : </strong><a target="_blank" title="Google Maps Link to Mono Cliffs" href="https://www.google.ca/maps/place/Mono+Cliffs+Provincial+Park/@44.0278845,-80.062594,15.75z/data=!4m5!3m4!1s0x882af8b606945571:0xaf01be5eec6b693d!8m2!3d44.0477948!4d-80.0696976">Google Map Link</a></p>
    <p><strong>Route : </strong> For this hike we parked just down 3rd Line near Dufferin road 8. If you look across 8 you can see the ladder where the trail begins.</p>

    <p>This loop includes a fair bit of very hilly climbs. The terrain is changing all the time... from hills, to forest, to meadows, to lakeside. It is one of our favourite loops. It also includes great view from the Lookout, and McCarston Lake. The photographic gems were at the end with a few still ponds that were mirroring the surrounding trees. </p>
