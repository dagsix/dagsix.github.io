    <p>The Great Trail, formerly known as Trans Canada Trail, is a cross-Canada system of greenways, waterways and roadways that stretches from the Atlantic to the Pacific to the Arctic oceans. The Trail extends over 24,000 kilometres (15,000 miles); it is now the longest recreational, multi-use trail network in the world. The idea for the Trail began in 1992, shortly after the Canada 125 celebrations. Since then it has been supported by donations from individuals, corporations, foundations and all levels of government.</p>

    <p>Trans Canada Trail (TCT) is the name of the non-profit group that raises funds for the continued development of the Trail. However, the Trail is owned and operated at the local level.</p>

    <p>On August 26, 2017, TCT celebrated the connection of the Trail with numerous events held throughout Canada. TCT has said it now plans to make the Trail more accessible, replace interim roadways with off-road greenways, add new spurs and loops to the Trail and fund emergency repairs when needed.</p>
