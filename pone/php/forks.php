    <span class="subtitle">Forks of the Credit</span>

    <br><br>
    <p><strong>Difficulty : </strong>Intermediate</p>
    <p><strong>Duration : </strong>2 hrs</p>
    <p><strong>Location : </strong><a target="_blank" title="Google Maps Link to Forks of the Credit" href="https://www.google.ca/maps/place/Forks+of+the+Credit+Provincial+Park/@43.8278571,-80.0023767,18z/data=!4m5!3m4!1s0x882b060753739fe1:0x9c502783af8c4c4b!8m2!3d43.8250707!4d-80.0041577">Google Map Link</a></p>
    <p><strong>Route : </strong>Follow HWY 10 north to Forks of the Credit Road (on your left). It is a windy often busy road so take your time and enjoy the scenery. We parked on Forks of the Credit road beside the river just before Dominion Road. Walk down Dominion Road until you reach the path to the trails. We followed the Bruce Trail which was left off the entrance trail.</p>

    <p>About the hike: It is a nice hike offering changing scenery and a fair bit of water access. The first section of the trail follows the river. This section is mostly flat. </p>
