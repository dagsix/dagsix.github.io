    <p><strong>Difficulty : </strong>Intermediate (long climb at the end)</p>
    <p><strong>Duration : </strong>1 1/2 hrs</p>
    <p><strong>Location : </strong><a target="_blank" title="Google Maps Link to Forks of the Credit" href="https://www.google.ca/maps/place/43%C2%B056'15.9%22N+79%C2%B056'07.2%22W/@43.93775,-79.937522,17z/data=!3m1!4b1!4m6!3m5!1s0x0:0x0!7e2!8m2!3d43.9377614!4d-79.9353404?hl=en">Google Map Link</a></p>
    <p><strong>Route : </strong> There is some parking along the side of the road on Coolihans Side Road, just past Glen Haffy road. There is room for 5 or so cars. You should be able to see the "trail ladder" from the road. </p>

    <p>While this loop does follow parts of the Red and Blue Glen Haffy trails, you'll always simply be following the white Bruce Trail blazes. The trail will eventually exit into a hilly meadow. Follow along with the right edge of the meadow/forest until you see the white blazes continue into to the forest. It isn't too far from the entrance into the meadow. The trail follows down into some incredible forest area. There are several little bridges to cross. Occasionally the trail will fork off to a red, blue or unidentified trail. Just follow the white blazes.</p>


    <p>There are a few areas where the trail emerged out from the forest, there is even a vista, which should be nice in the Fall.</p>

    <p>You can turn around where ever you want... we walked until the trail hit Highway 9 then turned around and went back the way we came. </p>

    <p>You can use the "back enterance" by parking along Glen Haffy Road and enter near the #10 marker.
        Our route (which has become the 'usual' route) is #10 > #9 > #11 > #4 > #3 > #2 > #1 then back again to form a loop. </p>





